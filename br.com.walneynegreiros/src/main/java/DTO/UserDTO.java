package DTO;

import Entidades.User;

public class UserDTO {
	@Id
	private Long id;
	private String name;

	public UserDTO(User user) {
		super();
		this.id = id;
		this.name = name;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
